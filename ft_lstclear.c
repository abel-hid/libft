#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*new;

	if (!lst || !del)
		return ;
	while (*lst)
	{
		new = *lst;
		*lst = (*lst)->next;
		ft_lstdelone(new, del);
	}
}
